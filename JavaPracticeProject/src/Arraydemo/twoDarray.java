package Arraydemo;

public class twoDarray {

	public static void main(String[] args) {

		//row and column
		int student_name[][] = new int[2][2];
		
		student_name[0][0]=10;
		student_name[0][1]=20;
		student_name[1][0]=30;
		student_name[1][1]=40;
		
		System.out.println("The value is" + student_name[0][1]);
		
	}

}
